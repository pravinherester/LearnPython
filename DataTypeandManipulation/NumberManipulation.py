print("Rounding =====")
print(8/3)
print(round(8/3)) # prints number rounding to nearest integer number 2.6666666666666665 ->3  or 2.4-->2
print(round(8/3,2)) # prints number rounding to nearest floating number 2.6666666666666665 ->2.67 
print("=====")

print("Floor division===")
print(8//3)
print(type(8//3))
print("======")

print("Modulo operator====")
print(8%3)
print(type(8%3))
print("======")

print("Assignment operators ====")
print(4/2)
result=4/2
result/=2 # is equivalent to  result = result / 2
print(result)

print(4*2)
result=4*2
result*=2     # is equivalent to  result = result ** 2
print(result)

print(4+2)
result=4+2
result+=2      # is equivalent to  result = result + 2
print(result)

print(4+2)
result=4-2
result-=2     # is equivalent to  result = result - 2
print(result)
print("======")